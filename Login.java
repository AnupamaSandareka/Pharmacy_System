    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = txtEmail.getText();
        String password = String.valueOf(txtPassword.getPassword());
        
        int temp =0;
        
        try{
            Connection con = ConnectionProvider.getConnection();
            Statement statement = con.createStatement();
            ResultSet result = statement.executeQuery("select * from appUser where email='"+email+"' and password='"+password+"'");
            while(result.next()){
                temp = 1;
                if(result.getString("userRole").equals("Admin")){
                    setVisible(false);
                    new AdminDashboard(email).setVisible(true);
                }
                else{
                    setVisible(false);
                    new PharmacistDashboard(email).setVisible(true);
                    
                }
            }
            
            if(temp==0){
                JOptionPane.showMessageDialog(null,"Incorrect email or password.");
                txtEmail.setText("");
                txtPassword.setText("");
            }
            
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

